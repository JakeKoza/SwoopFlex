public class WorkoutModel {

   private String work_ID, work_Name, work_Type, work_Descrip, work_Video, work_Time, equip_ID;

    public String getWork_ID() {
        return work_ID;
    }

    public void setWork_ID(String work_ID) {
        this.work_ID = work_ID;
    }

    public String getWork_Name() {
        return work_Name;
    }

    public void setWork_Name(String work_Name) {
        this.work_Name = work_Name;
    }

    public String getWork_Type() {
        return work_Type;
    }

    public void setWork_Type(String work_Type) {
        this.work_Type = work_Type;
    }

    public String getWork_Descrip() {
        return work_Descrip;
    }

    public void setWork_Descrip(String work_Descrip) {
        this.work_Descrip = work_Descrip;
    }

    public String getWork_Video() {
        return work_Video;
    }

    public void setWork_Video(String work_Video) {
        this.work_Video = work_Video;
    }

    public String getWork_Time() {
        return work_Time;
    }

    public void setWork_Time(String work_Time) {
        this.work_Time = work_Time;
    }

    public String getEquip_ID() {
        return equip_ID;
    }

    public void setEquip_ID(String equip_ID) {
        this.equip_ID = equip_ID;
    }
}
